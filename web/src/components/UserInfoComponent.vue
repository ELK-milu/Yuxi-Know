<script setup>
import { computed, ref } from 'vue';
import { useRouter } from 'vue-router';
import { useUserStore } from '@/stores/user';
import { UserOutlined, LogoutOutlined, UploadOutlined } from '@ant-design/icons-vue';
import { message } from 'ant-design-vue';
import { CircleUser, UserRoundCheck } from 'lucide-vue-next';

const router = useRouter();
const userStore = useUserStore();

// 个人资料弹窗状态
const profileModalVisible = ref(false);
const avatarUploading = ref(false);
const profileEditing = ref(false);
const editedProfile = ref({
  phone_number: ''
});

const props = defineProps({
  showRole: {
    type: Boolean,
    default: false
  },
  showButton: {
    type: Boolean,
    default: false
  }
})

// 用户名首字母（用于显示在头像中）
const userInitial = computed(() => {
  if (!userStore.username) return '?';
  return userStore.username.charAt(0).toUpperCase();
});

// 用户角色显示文本
const userRoleText = computed(() => {
  switch (userStore.userRole) {
    case 'superadmin':
      return '超级管理员';
    case 'admin':
      return '管理员';
    case 'user':
      return '普通用户';
    default:
      return '未知角色';
  }
});

// 用户角色徽章样式类
const userRoleClass = computed(() => {
  return {
    'superadmin': userStore.userRole === 'superadmin',
    'admin': userStore.userRole === 'admin',
    'user': userStore.userRole === 'user'
  };
});

// 退出登录
const logout = () => {
  userStore.logout();
  message.success('已退出登录');
  // 跳转到首页
  router.push('/login');
};

// 前往登录页
const goToLogin = () => {
  router.push('/login');
};

// 打开个人资料页面
const openProfile = async () => {
  profileModalVisible.value = true;
  profileEditing.value = false;

  // 刷新用户信息并初始化编辑表单
  try {
    await userStore.getCurrentUser();
    editedProfile.value = {
      phone_number: userStore.phoneNumber || ''
    };
  } catch (error) {
    console.error('刷新用户信息失败:', error);
  }
};

// 角色标签颜色
const getRoleColor = (role) => {
  switch (role) {
    case 'superadmin': return 'red';
    case 'admin': return 'blue';
    case 'user': return 'green';
    default: return 'default';
  }
};

// 开始编辑个人资料
const startEdit = () => {
  profileEditing.value = true;
  editedProfile.value = {
    phone_number: userStore.phoneNumber || ''
  };
};

// 取消编辑
const cancelEdit = () => {
  profileEditing.value = false;
  editedProfile.value = {
    phone_number: userStore.phoneNumber || ''
  };
};

// 保存个人资料
const saveProfile = async () => {
  try {
    // 验证手机号格式
    if (editedProfile.value.phone_number && !validatePhoneNumber(editedProfile.value.phone_number)) {
      message.error('请输入正确的手机号格式');
      return;
    }

    await userStore.updateProfile(editedProfile.value);
    message.success('个人资料更新成功！');
    profileEditing.value = false;
  } catch (error) {
    console.error('更新个人资料失败:', error);
    message.error('更新失败：' + (error.message || '请稍后重试'));
  }
};

// 手机号验证
const validatePhoneNumber = (phone) => {
  if (!phone) return true; // 空手机号允许
  const phoneRegex = /^1[3-9]\d{9}$/;
  return phoneRegex.test(phone);
};

// 头像上传前验证
const beforeUpload = (file) => {
  const isImage = file.type.startsWith('image/');
  if (!isImage) {
    message.error('只能上传图片文件！');
    return false;
  }

  const isLt5M = file.size / 1024 / 1024 < 5;
  if (!isLt5M) {
    message.error('图片大小不能超过 5MB！');
    return false;
  }

  return true;
};

// 处理头像上传
const handleAvatarChange = async (info) => {
  if (info.file.status === 'uploading') {
    avatarUploading.value = true;
    return;
  }

  if (info.file.status === 'done') {
    avatarUploading.value = false;
    return;
  }

  // 手动处理文件上传
  try {
    avatarUploading.value = true;
    const result = await userStore.uploadAvatar(info.file.originFileObj || info.file);
    message.success('头像上传成功！');
  } catch (error) {
    console.error('头像上传失败:', error);
    message.error('头像上传失败：' + (error.message || '请稍后重试'));
  } finally {
    avatarUploading.value = false;
  }
};
</script>

